export function recommendGames({ players, vibe, duration }) {
  const preset = PLAYER_PRESETS.find(p => p.id === players)
  const dur = DURATION_PRESETS.find(d => d.id === duration)
  return GAMES
    .filter(g => {
      const playerOk = !preset || (g.playerRange[0] <= preset.range[1] && g.playerRange[1] >= preset.range[0])
      const durOk = !dur || g.durationMin <= dur.max
      const vibeOk = !vibe || g.vibes.includes(vibe)
      return playerOk && durOk && vibeOk
    })
    .sort((a, b) => (b.hot ? 1 : 0) - (a.hot ? 1 : 0))
    .slice(0, 4)
}
