export function recommendGames({ players, vibe, duration }) {
  const preset = PLAYER_PRESETS.find(p => p.id === players)
  const dur    = DURATION_PRESETS.find(d => d.id === duration)

  const scored = GAMES.map(game => {
    let score = 0

    /* ── 1. Vibe (0–40 pts) ── */
    if (vibe) {
      if (game.vibes.includes(vibe)) {
        score += 40
      } else {
        const nearVibes = VIBE_AFFINITY[vibe] || []
        const overlap = game.vibes.filter(v => nearVibes.includes(v)).length
        score += overlap * 10  // up to 30 pts for close vibes
      }
    } else {
      score += 20 // no preference → neutral
    }

    /* ── 2. Player count (0–30 pts) ── */
    if (preset) {
      const [pMin, pMax] = preset.range
      const [gMin, gMax] = game.playerRange
      if (gMin <= pMin && gMax >= pMax) {
        score += 30 // perfect bracket
      } else if (gMax >= pMin && gMin <= pMax) {
        const overlap = Math.min(gMax, pMax) - Math.max(gMin, pMin)
        const span    = pMax - pMin || 1
        score += Math.round(20 * Math.min(overlap / span, 1))
      }
      // 0 pts if no overlap at all
    } else {
      score += 15
    }

    /* ── 3. Duration (0–20 pts) ── */
    if (dur) {
      if (game.durationMin <= dur.max * 0.7) score += 20
      else if (game.durationMin <= dur.max)   score += 12
      else                                     score -= 8  // over budget
    } else {
      score += 10
    }

    /* ── 4. Hot bonus (0–8 pts) ── */
    if (game.hot) score += 8
    return { ...game, _score: score }
  })

  // Sort descending by score
  const sorted = scored
    .filter(g => g._score > 0)
    .sort((a, b) => b._score - a._score)

  // Pick top 4 with category diversity (avoid showing 3 "голосование" games)
  const result = []
  const usedCategories = new Set()
  for (const g of sorted) {
    if (result.length >= 4) break
    // Allow at most 2 per category
    const catCount = [...result].filter(r => r.category === g.category).length
    if (catCount < 2) {
      result.push(g)
      usedCategories.add(g.category)
    }
  }
  // Fill remaining slots without category restriction
  for (const g of sorted) {
    if (result.length >= 4) break
    if (!result.find(r => r.id === g.id)) result.push(g)
  }

  return result.slice(0, 4)
}
